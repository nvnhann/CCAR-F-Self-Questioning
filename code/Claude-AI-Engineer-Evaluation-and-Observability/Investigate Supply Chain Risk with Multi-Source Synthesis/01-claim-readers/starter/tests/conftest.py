"""Shared test fixtures.

`FixtureNewsExtractor` replays recorded Claude extraction output (generated by a
real sub-agent role-play, stored in fixtures/news_extraction/) so the suite runs
offline without an API key — the allowed "recorded-response fake" at the API
boundary.
"""
from __future__ import annotations

import json
from pathlib import Path

import pytest

from supply_chain_risk.models import Claim
from supply_chain_risk.readers import read_audit, read_logistics, read_news, read_quality

ROOT = Path(__file__).resolve().parent.parent
DATA = ROOT / "data" / "meridian"
NEWS_DIR = DATA / "news"
FX_DIR = ROOT / "fixtures" / "news_extraction"


class FixtureNewsExtractor:
    """Maps an article's exact text to its recorded extraction claims."""

    def __init__(self, news_dir: Path, fixture_dir: Path) -> None:
        self._by_text: dict[str, list[dict[str, object]]] = {}
        for article in news_dir.glob("*.txt"):
            fixture = fixture_dir / f"{article.stem}.json"
            self._by_text[article.read_text().strip()] = json.loads(fixture.read_text())

    def extract(self, article_text: str) -> list[dict[str, object]]:
        return self._by_text[article_text.strip()]


@pytest.fixture
def data_dir() -> Path:
    return DATA


@pytest.fixture
def news_dir() -> Path:
    return NEWS_DIR


@pytest.fixture
def news_extractor() -> FixtureNewsExtractor:
    return FixtureNewsExtractor(NEWS_DIR, FX_DIR)


def load_all_claims() -> list[Claim]:
    """All claims from every source (audit, logistics, quality, news)."""
    extractor = FixtureNewsExtractor(NEWS_DIR, FX_DIR)
    claims: list[Claim] = []
    claims += read_audit(DATA / "audit.json").claims
    claims += read_logistics(DATA / "logistics.csv").claims
    claims += read_quality(DATA / "quality.sqlite").claims
    for article in ("port_strike", "quality_recall", "ambiguous_meridian"):
        claims += read_news(NEWS_DIR / f"{article}.txt", extractor).claims
    return claims


@pytest.fixture
def all_claims() -> list[Claim]:
    return load_all_claims()


@pytest.fixture(scope="module")
def seeded_memory():  # type: ignore[no-untyped-def]
    """A SharedMemory seeded with the full corpus (module-scoped: embed once)."""
    from supply_chain_risk.memory import SharedMemory

    mem = SharedMemory()
    mem.add_claims(load_all_claims())
    return mem
